import UIKit

// MARK: - Style Guide

extension UIColor {
    @nonobjc class var lightishBlue: UIColor {
        return #colorLiteral(red: 0.2549019608, green: 0.4666666667, blue: 0.9764705882, alpha: 1)
    }

    @nonobjc class var black: UIColor {
        return #colorLiteral(red: 0.1019607843, green: 0.1019607843, blue: 0.1019607843, alpha: 1)
    }

    @nonobjc class var greyishBrown: UIColor {
        return #colorLiteral(red: 0.262745098, green: 0.262745098, blue: 0.262745098, alpha: 1)
    }

    @nonobjc class var lavenderBlue: UIColor {
        return #colorLiteral(red: 0.5725490196, green: 0.5529411765, blue: 0.9764705882, alpha: 1)
    }
}
