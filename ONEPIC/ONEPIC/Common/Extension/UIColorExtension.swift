import UIKit

// MARK: - Style Guide

extension UIColor {
    @nonobjc class var lightishBlue: UIColor {
        return UIColor(red: 65.0 / 255.0, green: 119.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 26.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 67.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lavenderBlue: UIColor {
        return UIColor(red: 146.0 / 255.0, green: 141.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
}
